"""Модуль исключений для интеллектуальной системы.

Содержит пользовательские исключения для обработки ошибочных ситуаций
в различных компонентах системы.
"""


class IntelligentSystemException(Exception):
    """Базовое исключение для интеллектуальной системы.

    Атрибуты:
        message (str): Сообщение об ошибке.
        code (int): Код ошибки (по умолчанию 1).
    """

    def __init__(self, message: str, code: int = 1):
        """Инициализация исключения.

        Args:
            message: Сообщение об ошибке.
            code: Код ошибки.
        """
        self.message = message
        self.code = code
        super().__init__(self.message)


class SensorException(IntelligentSystemException):
    """Исключение, возникающее при ошибках в работе сенсоров.

    Используется для обработки ошибок при сборе данных,
    калибровке или недоступности сенсоров.
    """

    pass


class AlgorithmException(IntelligentSystemException):
    """Исключение, возникающее при ошибках алгоритмов.

    Используется для обработки ошибок при обучении,
    предсказании или обновлении алгоритмов машинного обучения.
    """

    pass


class StateException(IntelligentSystemException):
    """Исключение, возникающее при ошибках состояния системы.

    Используется для обработки ошибок при сохранении,
    загрузке или изменении состояния системы.
    """

    pass


class DataException(IntelligentSystemException):
    """Исключение, возникающее при ошибках работы с данными.

    Используется для обработки ошибок при добавлении,
    удалении или валидации данных.
    """

    pass


class FeedbackException(IntelligentSystemException):
    """Исключение, возникающее при ошибках обратной связи.

    Используется для обработки ошибок при сборе,
    валидации или применении обратной связи.
    """

    pass
